package FundamentosJavaDev;

public class Aula06EstruturasDeRepeticaoForExercicio {
    public static void main(String[] args) {

        for (int i = 0; i <= 10; i++) {
            if(i % 2 == 0){
                System.out.println("Os número pares são : " + i);
            }else{
                System.out.println("Os número ímpares são : " + i);
            }

        }
    }
}
